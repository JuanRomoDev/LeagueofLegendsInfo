package com.juanromodev.leagueoflegendsinfo.data.model.item;

public class Gold {

    private int base;
    private int total;
    private int sell;
    private boolean purchasable;

    public Gold() {

    }

    public int getBase() {
        return base;
    }

    public int getTotal() {
        return total;
    }

    public int getSell() {
        return sell;
    }

    public boolean isPurchasable() {
        return purchasable;
    }
}

package com.juanromodev.leagueoflegendsinfo.data.model.item;

public class Image {

    private String full;
    private String sprite;
    private String group;
    private int x;
    private int y;
    private int w;
    private int h;

    public Image() {

    }

    public String getFull() {
        return full;
    }

    public String getSprite() {
        return sprite;
    }

    public String getGroup() {
        return group;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getW() {
        return w;
    }

    public int getH() {
        return h;
    }
}
